package Do_while;

public class Syntax {

	public static void main(String[] args) {
		int i=1;                  // 1. check initialization
		do {                      // 2.Enter in do block
			System.out.println(i);// 3.print the statement
			i++;                  // 4.make increment
		}while(i<=10);            // 5.exit and check condition
                                  // 6.repeat until condition satisfy
	}

}
