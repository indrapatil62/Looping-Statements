package While_loop;

public class Syntax {

	public static void main(String[] args) {
		int i=1;                      // 1.it check the initialization statement
		while(i<=10) {                // 2.it will enter in while loop and check condition
			System.out.println(i);    // 3.it read sysout and print it
			i++;                      // 4.it will increment
		}                             // 5.again in while block again do step 2-4 till satisfy the conditon
	}

}
